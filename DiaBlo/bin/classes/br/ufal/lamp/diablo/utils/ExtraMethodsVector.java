package br.ufal.lamp.diablo.utils;

import java.util.Vector;

import br.ufal.lamp.diablo.core.Block;
import br.ufal.lamp.diablo.core.block.BeginTerminal;
import br.ufal.lamp.diablo.exception.BeginNotFoundException;

public class ExtraMethodsVector {	
	
	
	
	public static int countOccurrenceBeginTerminal(Vector<Block> vetor) {
		int count_begin = 0;
		for(int i = 0; i < vetor.size(); i++) {
			if (vetor.get(i) instanceof BeginTerminal) {
				count_begin++;
			}
		}
		return count_begin;
	}
	
	
}
